print("1 es mayor que 2", 1 > 2)
print("1 es menor que 2", 1 < 2)
print("1 es mayor o igual que 2", 1 >= 2)
print("1 es menor o igual que 2", 1 <= 2)
print("1 es igual que 2", 1 == 2)
print("1 es distinto que 2", 1 != 2)
print("Hola es igual que Hola", "Hola" == "Hola")
print("Hola es distinto que Hola", "Hola" != "Hola")
print("2 es igual que 2 en String", 2 == "2")  # Compara valor pero no tipo
